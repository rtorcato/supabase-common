// Copy the root CHANGELOG.md into apps/docs/docs/changelog.md with Docusaurus
// frontmatter, so semantic-release keeps owning a single canonical changelog
// while the docs site renders it in-nav. The output file is gitignored — it is
// regenerated on every docs build (wired as apps/docs's `prebuild` script).

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(here, '..')
const source = resolve(repoRoot, 'CHANGELOG.md')
const target = resolve(repoRoot, 'apps/docs/docs/changelog.md')

const frontmatter = `---
title: Changelog
description: Release notes for @rtorcato/supabase-common, generated by semantic-release.
---

`

// Before the first semantic-release run there is no root CHANGELOG.md; fall
// back to the same links the static page carried so the page never regresses.
const fallback = `# Changelog

Releases are automated with [semantic-release](https://semantic-release.gitbook.io/)
and published to npm and GitHub.

- 📦 [npm versions](https://www.npmjs.com/package/@rtorcato/supabase-common?activeTab=versions)
- 🏷️ [GitHub releases](https://github.com/rtorcato/supabase-common/releases)
`

let body = ''
try {
	body = readFileSync(source, 'utf8')
} catch {
	body = fallback
}

writeFileSync(target, frontmatter + body)
console.log(`sync-changelog: wrote ${target}`)
